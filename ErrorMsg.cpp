const char* ErrorMsg::GetErrorText(unsigned int dwErrCode)
{
  const char** pErrMsg = 0;
  int nMaxMsgNum = 0;
  switch (dwErrCode & ERR_TYPE_MASK) {
    case ERR_CMD:
      pErrMsg = ERR_CMD_TXT;
      nMaxMsgNum = sizeof(ERR_CMD_TXT) / sizeof(ERR_CMD_TXT[0]);
      break;
    case ERR_VAC:
      pErrMsg = ERR_VAC_TXT;
      nMaxMsgNum = sizeof(ERR_VAC_TXT) / sizeof(ERR_VAC_TXT[0]);
      break;
    case ERR_COM:
      pErrMsg = ERR_COM_TXT;
      nMaxMsgNum = sizeof(ERR_COM_TXT) / sizeof(ERR_COM_TXT[0]);
      break;
    case ERR_PER:
      pErrMsg = ERR_PER_TXT;
      nMaxMsgNum = sizeof(ERR_PER_TXT) / sizeof(ERR_PER_TXT[0]);
      break;
    case ERR_SW:
      pErrMsg = ERR_SW_TXT;
      nMaxMsgNum = sizeof(ERR_SW_TXT) / sizeof(ERR_SW_TXT[0]);
      break;
    default:
      break;
  }
  if (!pErrMsg) {
    return "Undefined error code in type!!\n";
  }
  int nVal = dwErrCode & ERR_VAL_MASK;
  if (nVal >= nMaxMsgNum) {
    return "Undefined error code in value!!\n";
  }
  return pErrMsg[nVal];
}
